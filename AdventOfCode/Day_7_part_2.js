const input = require("./input/Day_7");

function countQuantumPaths(grid) {
    const rows = grid.length;
    const cols = grid[0].length;

    const start = grid[0].indexOf('S');

    let current = Array(cols).fill(0);
    current[start] = 1;

    for(let r = 0; r < rows - 1; r++) {

        const next = Array(cols).fill(0);

        for(let c = 0; c < cols; c++) {

            if(current[c] === 0) continue;

            if(grid[r + 1][c] === "^") {
                if(c > 0) next[c - 1] += current[c];
                if(c < cols - 1) next[c + 1] += current[c];
            } else {
                next[c] += current[c];
            }

        }

        current = next;

    }

    let total = 0;
    for(const count of current) total += count;

    return total;

}

function solve(input) {

    const lines = input.split("\n");
    const grid = lines.map(line => line.split(""));

    return countQuantumPaths(grid);
}

